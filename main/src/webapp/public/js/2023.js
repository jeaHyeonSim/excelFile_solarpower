    function slicezz(list, name) {
        // console.log(list);
        let listRs =[];
        let sum = 0;
        let cnt = 0;
        for (let i = 0; i < list.length +1; i++) {
            if(cnt < 20) {
                cnt++;
            } else {
                listRs.push(Number(list[i]).toFixed(2));
                sum = 0;
                cnt = 0;
            }
    
            
        }
        // console.log(name);
        // console.log(listRs);
        return listRs;
    }
    
    
